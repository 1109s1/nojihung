package operText128;

public class OpTest3 {

	public static void main(String[] args) {
		// 논리연산자
		// 제어문(if)에서 많이 활용.
		int num1 = 100, num2 = 200;
		// ==같다
		System.out.println(" == 연산자: " + (num1 == num2 ));
		// != 다르다
		System.out.println("! = 연산자 :" + (num1 != num2));
		// >크다
		System.out.println("> 연산자 : " + (num1 > num2));
		// <작다
		System.out.println("< 연산자 : " + (num1 < num2));
		// 크거나 같다
		System.out.println(">= 연산자 : " + (num1 >= num2));
		// 작거나 같다
		System.out.println("<= 연산자 : " + (num1 <= num2));
		

	}

}
